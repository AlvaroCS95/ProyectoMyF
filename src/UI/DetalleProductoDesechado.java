    private void btAceptar_DetalleDesechoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAceptar_DetalleDesechoActionPerformed
       
      try{
            if(OpcionParaDesechar==0||textAreaDetalleDesecho.getText().equals("")){
            getToolkit().beep();
            JOptionPane.showMessageDialog(null, "Primero debe eligir una de las opciones o ingresar el detalle del desecho","ERROR",JOptionPane.ERROR_MESSAGE);
            return;
            }else{
                    if(OpcionParaDesechar==1){
                    
                    CantidadParaDesechar=Integer.parseInt(txtCantidadParaDevolver_DetalleDesecho.getText());
                                if(CantidadParaDesechar>CantidadMaximaParaVender){
                                   getToolkit().beep();
                                    JOptionPane.showMessageDialog(null, "Solo puede ingresar una cantidad menor a:"
                                    + " "+CantidadMaximaParaVender+"\nPor"
                                    + "favor vuelva a ingresar una cantidad menor, o seleccione otra opcion.","ERROR",JOptionPane.ERROR_MESSAGE);
                                    return;
                                    }else{
                                    AgregarProductosADesecho(CodigoProducto,NombreProducto,CantidadParaDesechar,textAreaDetalleDesecho.getText());
                                    }
                    }
                    else{
                    CantidadParaDesechar=CantidadMaximaParaVender;
                    AgregarProductosADesecho(CodigoProducto,NombreProducto,CantidadParaDesechar,textAreaDetalleDesecho.getText());
                    }
            }
            
            dispose();
        } catch (SQLException ex) {
            Logger.getLogger(DetalleProductoDesechado.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btAceptar_DetalleDesechoActionPerformed

